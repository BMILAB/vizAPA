# getMappedChr('chr1', c('chr1','chr2','xxxx'), data.frame(c1=1:3, c2=paste0('chr',1:3))) #chr1
# getMappedChr('1', c('chr1','chr2','xxxx'), data.frame(c1=1:3, c2=paste0('chr',1:3))) #chr1
